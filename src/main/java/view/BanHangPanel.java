    public void loadTableSanPham(List<Laptop> listLp){
        int index = 1;
        model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for (Laptop l : listLp) {
            model.addRow(new Object[]{index,l.getMa(),l.getImei(),l.getTen(),l.getMa(),l.getGiaBan()});
            index ++;
        }
    }
    
