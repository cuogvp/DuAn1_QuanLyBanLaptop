    private void tbKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKHMouseClicked
        int rows = tbKH.getSelectedRow();
        KhachHang u = iKhachHangService.selectAll().get(rows);
        txtMaKH.setText(u.getMa());
        txtTenKH.setText(u.getTen());
        txtNgaySinhKH.setText(u.getNgaySinh());
        if(u.isGioiTinh()){
            rdNamKH.setSelected(true);
        }else{
            rdNuKH.setSelected(true);
        }
        txtSdtKH.setText(u.getSdt());
        txtDiaChiKH.setText(u.getDiaChi());  
        txtMKKH.setText(u.getMatKhau());
        lbIDKH.setText(u.getHinhAnh());
        
    }//GEN-LAST:event_tbKHMouseClicked

