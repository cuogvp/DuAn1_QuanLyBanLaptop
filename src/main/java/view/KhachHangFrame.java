    public void loadData(List<KhachHang> listKH){
        model = (DefaultTableModel) tbKH.getModel();
        model.setRowCount(0);
        for (KhachHang kh : listKH) {
            model.addRow(new Object[]{kh.getMa(),kh.getTen(),kh.getNgaySinh()
            ,kh.isGioiTinh()==true?"Nam":"Nữ",kh.getSdt(),kh.getDiaChi(),kh.getThanhPho()
            ,kh.getMatKhau(),kh.getHinhAnh(),kh.getQuocGia()});
        }
    }
    
    public KhachHang getData(){
        KhachHang kh = new KhachHang();
        String ma = txtMaKH.getText().trim();
        kh.setMa(ma);
        String ten = txtTenKH.getText().trim();
        kh.setTen(ten);
        String ngaySinh = txtNgaySinhKH.getText().trim();
        kh.setNgaySinh(ngaySinh);
        boolean gioiTinh;
        if(rdNamKH.isSelected()){
            gioiTinh = true;
        }else{
            gioiTinh = false;
        }
        kh.setGioiTinh(gioiTinh);
        String sdt = txtSdtKH.getText().trim();
        kh.setSdt(sdt);
        String diaChi = txtDiaChiKH.getText().trim();
        kh.setDiaChi(diaChi);
        String tp = txtThanhPhoKH.getText().trim();
        kh.setSdt(sdt);
        String qg = txtQGKH.getText().trim();
        kh.setSdt(sdt);
        String matKhau = txtMKKH.getText().trim();
        kh.setMatKhau(matKhau);
        kh.setHinhAnh("NO AVATAR");
        return kh;
        
    }

